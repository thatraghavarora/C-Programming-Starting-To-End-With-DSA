#include <stdio.h>
int main() {
    int a = 10; int b = 20;
    printf("Relational Operators (a = %d, b = %d)\n", a, b);
    printf("a == b -> %d\n", a == b);
    printf("a != b -> %d\n", a != b);
    printf("a >  b -> %d\n", a > b);
    printf("a <  b -> %d\n", a < b);
    printf("a >= b -> %d\n", a >= b);
    printf("a <= b -> %d\n\n", a <= b);
}